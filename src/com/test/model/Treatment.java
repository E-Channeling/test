package com.test.model;

public class Treatment {
	private String petinatId;
	private String doctorId;
	private String tratmentDescription;
	private String dateOfTreatment;
	
	
	public String getPetinatId() {
		return petinatId;
	}
	public void setPetinatId(String petinatId) {
		this.petinatId = petinatId;
	}
	public String getDoctorId() {
		return doctorId;
	}
	public void setDoctorId(String doctorId) {
		this.doctorId = doctorId;
	}
	public String getTratmentDescription() {
		return tratmentDescription;
	}
	public void setTratmentDescription(String tratmentDescription) {
		this.tratmentDescription = tratmentDescription;
	}
	public String getDateOfTreatment() {
		return dateOfTreatment;
	}
	public void setDateOfTreatment(String dateOfTreatment) {
		this.dateOfTreatment = dateOfTreatment;
	}
	

}
